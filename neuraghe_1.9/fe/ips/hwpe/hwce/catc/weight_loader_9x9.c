void weight_loader_9x9(uint32_t weight_tcdm[82/2+1], uint16_t weight_priv[82], int size, int mask) {
  for(int i=0; i<82/2; i++) {
    weight_priv[i*2]   = ((uint16_t) ((weight_tcdm[i] & 0xffff0000) >> 16) ) >> ( (size<=9) ? 0 :
      ( (mask==0) ? mask_11x11_0[i*2]   : (mask==1) ? mask_11x11_1[i*2]   : (mask==2) ? mask_11x11_2[i*2]   : mask_11x11_3[i*2]  ) );
    weight_priv[i*2+1] = ((uint16_t) (weight_tcdm[i] & 0x0000ffff)         ) >> ( (size<=9) ? 0 :
      ( (mask==0) ? mask_11x11_0[i*2+1] : (mask==1) ? mask_11x11_1[i*2+1] : (mask==2) ? mask_11x11_2[i*2+1] : mask_11x11_3[i*2+1]) );
  }
  weight_tcdm[82/2] = 1;
  return;
}
